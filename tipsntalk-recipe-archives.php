    <h3 class="recipe-header">Mini Sesame Baguettes</h3>
    <p>These easy-to-make, airy, sesame baguettes originated as a family favourite sesame roll. I used ¼ of this recipe for one of the Leku&eacute; baguette makers. You may need a mixer if you do the full recipe, but not for the smaller amount. If you are doing the smaller amount, just leave out the egg, and put a bit more oil.</p>

<h4>Ingredients</h4>
<ul class="ingredients">
<li>2 ½ cups warm water </li>
<li>½ cup sugar </li>
<li>2 tablespoons yeast</li>
<li>1 kg all purpose flour</li>
<li>1 tablespoon salt</li>
<li>1 egg</li>
<li> ½ cup oil</li>

</ul>

<h4>Directions</h4>
<ol class="ingredients">
<li>Proof the yeast as per instructions above. </li>
<li>Mix dry ingredients.</li>
<li>Add the water/yeast mixture, oil and egg.</li>
<li>Knead until you have a soft, smooth dough.</li>
<li>Place in a warm place, and let rise until double.</li>
<li>Divide the dough into 4 parts. Roll out each part into a rectangle, a little smaller than the length of the baguette pan, and roll up lengthwise.</li>
<li>Dip into water, and then sesame seeds.</li>
<li>Place into baguette pans, and let rise again until doubled. </li>
<li>Make two diagonal cuts along top of each roll.</li>
<li>Bake at 350&deg; for 20-30 minutes.</li>
</ol>
<p><strong>Note:</strong> The baguette pan can be put straight on the oven rack, but you may want to put something under it, so you don&rsquo;t have sesame seeds all over the bottom of the oven. I know that from experience!</p>


