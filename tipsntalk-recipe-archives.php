    <h3 class="recipe-header">Spelt Bread</h3>
    <p>I eat this bread for breakfast almost every day. It is very satisfying, and even this small loaf lasts me a few days and it freezes very well.</p>

<h4>Ingredients</h4>
<ul class="ingredients">
<li>150 ml very warm water </li>
<li>2 ¼ teaspoon demerara sugar </li>
<li>¾ teaspoon instant yeast <small>(I prefer the Instaferm brand)</small></li>
<li>250 grams spelt flour, about half whole spelt and half light spelt. <small>(This is approximately 1 ¾ Cups, but weighing makes more sense, as not all flours are equal in weight.)</small></li>
<li>¾ teaspoon dough enhancer <small>(This is an Israeli product, and if you can’t get your hand on it, use ¼ teaspoon gluten.)</small></li>
<li>½ teaspoon salt</li>
<li>2 teaspoons oil</li>
<li>Challah topping</li>

</ul>

<h4>Directions</h4>
<ol class="ingredients">
<li>Proof the yeast as per instructions above. </li>
<li>Place the flour in the small Lekue bread maker. Add the salt and dough enhancer/gluten. Mix together.</li>
<li>Put the oil into the yeast water mixture, and then pour into the dry ingredients.</li>
<li>Mix together, and then knead by hand in the breadmaker, just until dough feels combined and soft.</li>
<li>Shape into log and sprinkle with challah topping.</li>
<li>Close bread maker, and place in a warm place to rise for an hour.</li>
<li>Dip into water, and then sesame seeds.</li>
<li>Place into baguette pans, and let rise again until doubled. </li>
<li>Make two diagonal cuts along top of each roll.</li>
<li>Bake at 375&deg; for about 35 minutes.</li>
</ol>
<p><strong>Note:</strong> Spelt dough is sensitive, and doesn’t like to be handled too much! Do not try to punch down the dough, or egg it when risen – it just won’t go back up again! Don’t open the bread maker either – just put gently into the oven!</p>


