    <h3 class="recipe-header">Caraway Rye Bread</h3>
    <p>This bread was served as rolls at a school dinner many years ago that was catered by the home economics class. I wish I knew who the teacher was, so that I could thank her for this recipe!</p>

<h4>Ingredients</h4>
<ul class="ingredients">
<li>1 cup hot water</li>
<li>A pinch of sugar</li>
<li>1 tablespoon yeast</li>
<li>1 ½ cups rye flour</li>
<li>2 cups white flour</li>
<li>2 tablespoons wheat germ</li>
<li>2 tablespoons oat bran</li>
<li>½ tablespoon Kosher salt</li>
<li>1 ½ tablespoons caraway seeds, plus more for sprinkling</li>
<li>1/3 cup equal parts oil and honey</li>
<li>1 egg, beaten</li>

</ul>

<h4>Directions</h4>
<ol class="ingredients">
<li>Proof the yeast as per instructions above, using the water and pinch of sugar and yeast.</li>
<li>Place all the dry ingredients in the large Lekue Bread Maker and mix well.</li>
<li>Pour the yeast mixture, oil and honey and egg into the flour mixture. <small>(You can leave over a bit of the egg for the top of the bread.)</small></li>
<li>Mix by hand, and knead well.</li>
<li>Form log, and then close bread maker.</li>
<li>Let rise in warm place until double – about 60 to 90 minutes.</li>
<li>Open the bread maker and egg the top with the left over egg mixed with water. Sprinkle with caraway seeds.</li>
<li>Place into baguette pans, and let rise again until doubled. </li>
<li>Make two diagonal cuts along top of each roll.</li>
<li>Bake at 375&deg; for about 40 minutes, until very brown.</li>
</ol>
<p><strong>Note:</strong> I bake this open, because this dough just needs a little more ‘elbow room.’ The round shape looks good for this kind of bread!</p>


