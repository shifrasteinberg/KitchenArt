    <h3 class="recipe-header">APPLE PIE</h3>
  <p>Loads of apples in a perfect pastry.</p>

<h4>Ingredients</h4>
<p><strong>Crust</strong></p>
<ul class="ingredients">
<li>2 cups flour


</li>
<li>1/2 teaspoon salt</li>
<li>2/3 cup margarine or shortening</li>
<li>
5-7 tablespoons ice water</li>
<li>egg wash</li>
</ul>

<p><strong>Filling</strong></p>
<ul class="ingredients">
<li>1 cup sugar</li>
    <li>2 tbsp flour</li>
    <li>1/2 teaspoon cinnamon</li>
    <li>*1 tin apple pie filling (or cherry pie filling)</li>
    <li>*2-3 grated apples</li>
    <li>juice of half a lemon</li>
</ul>

<h4>Directions</h4>
<ol class="ingredients">
<li>To make the crust, stir the flour and salt together. With a pastry blender, cut in the margarine or shortening until pieces are the size of small peas. Add the ice water, and toss until all the flour is moistened and mixture starts to form a ball. Divide the dough into two parts.</li>
<li>Roll out one part of the dough, and place in a 9-10 inch pie plate. Brush with a mixture made with one egg and two teaspoons water. (This prevents bottom dough from getting soggy.)</li>
<li>Combine the filling ingredients and spoon into pastry lined pan.</li>
<li>Roll out the second half of the dough. Use an apple pie topper to get the effect shown, and trim with pastry leaves.</li>
<li>Bake at 425&deg; for 45-50 minutes.</li>

</ol>
