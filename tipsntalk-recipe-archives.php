    <h3 class="recipe-header">Best Brownie Cheesecake</h3>
  

<h4>Ingredients</h4>
<p><strong>Bottom</strong></p>
<ul class="ingredients">
<li>4 eggs</li>
<li>1 1/4 cup sugar</li>
<li>1 teaspoon baking powder</li>
<li>Dash of salt</li>
<li>1 cup flour</li>
<li>1 cup chopped pecans
</li><li>1/2 teaspoon almond extract</li>
<li>1 teaspoon vanilla extract</li>
<li>6 oz. baking chocolate melted with 1 tablespoon butter</li>
</ul>

<p><strong>Top</strong></p>
<ul class="ingredients">
<li>2.5 containers whipped cream cheese</li>
<li>1 stick butter (minus 1 tablespoon)</li>
<li>1 cup sugar</li>
<li>2 eggs</li>
<li>1 teaspoon vanilla extract</li>
<li>2 tablespoons flour</li>
</ul>

<h4>Directions</h4>
<ol class="ingredients">
<li>Preheat oven to 350°. Grease or line a 9x13 pan.</li>
<li>For bottom: Mix all ingredients except pecans. Set aside 1/2 cup batter. Add pecans to main batter. Bake for 10 minutes.</li>
<li>For top: Cream cream cheese; add ingredients one at a time. Pour over brownie. Marbelize with reserved batter.</li>
<li>Bake for 45-60 minutes. Cool and cut into squares or rectangles.</li>
<li>Serve frosty.</li>
</ol>

