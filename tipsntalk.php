    <h3 class="recipe-header">Cherry Hamantash Muffins</h3>
  <p>Here's a recipe that will give you that special Purim hamantash look, without rolling out any dough! They are made in silicone triangle baking cups; feel free to make them all year round in any shape.</p>

<h4>Ingredients</h4>

<ul class="ingredients">
<li> 1 3/4 cups flour
</li>
<li>1/2 cup sugar</li>
<li>2 teaspoons baking powder</li>
<li>1/2 teaspoon salt</li>
<li>1/4 teaspoon baking soda</li>
<li>1 teaspoon lemon zest</li>
<li>1 egg</li>
<li>3/4 cup buttermilk or soy buttermilk (mix 1 tablespoon vinegar with enough milk to equal 3/4 cup and let stand 1 minute)</li>
<li>1/3 cup oil</li>
<li>1 teaspoon vanilla extract</li>
<li>1 1/2 cups cherry pie filling</li>
</ul>


<h4>Directions</h4>
<p>Preheat the oven to 375&deg;F. Lay out 12 triangle baking cups on a cookie sheet.</p>

<p>Mix all dry ingredients until well blended. Make a well in the center. Add the egg, buttermilk, oil, and vanilla extract and mix until just combined.</p>

<p>Spoon the batter into the baking cups, about one third of the way up. Mound one tablespoon of cherry pie filling in the center of the batter in each cup. Pipe the remaining batter around the edge of each muffin, forming a slight arc on each side.</p>

<p>Bake for 25-30 minutes. Yield: 12 muffins.</p>

