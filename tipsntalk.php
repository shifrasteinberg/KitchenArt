   <h3 class="recipe-header">Good-For-You Muffins</h3>
<p>Bake these healthy muffins in silly-feet silicone muffin cups, and your children will beg for more!</p>

<h4>Ingredients</h4>
	<p>Muffins</p>
<ul class="ingredients">

   <li> 3/4 cup flour
</li>
<li>3/4 cup whole wheat flour</li>
<li>1/2 cup wheat germ</li>
<li>3/4 cup sugar</li>
<li>2 teaspoons cinnamon</li>
<li>2 teaspoons baking soda</li>
<li>1/2 teaspoon salt</li>
<li>1/4 teaspoon ground cloves</li>
<li>1 1/2 cups shredded carrot</li>
<li>1/2 cup coconut</li>
<li>1/2 cup dried cranberries or raisins</li>
<li>1/2 cup sunflower seeds, chopped pumpkin seeds or chopped pecans</li>
<li>3/4 cup oil</li>
<li>1/4 cup milk or soymilk</li>
<li>2 teaspoons vanilla extract</li>
<li>2 eggs</li>
  </ul>
	
	<p>Optional Frosting</p>
	<ul class="ingredients">
		
<li> 1/2 cup cream cheese</li>
<li>1 cup icing sugar</li>
<li>2-3 teaspoons apple juice or milk</li>
</ul>

<h4>Directions</h4>
<p>Preheat the oven to 350&deg;F. </p>

<p>Combine the first seven ingredients in a large bowl. Add the apples, carrots, coconut, cranberries, and seeds or nuts; mix well. Add the remaining ingredients; stir just until dry ingredients are moistened.</p>

<p>Fill the muffin cups three quarters full.</p>

<p>Bake for 20-25 minutes, or until a toothpick inserted in the center comes out clean.</p>

<p>Beat the frosting ingredients until smooth. Spread on cooled muffins. Insert a clown head into each muffin, or decorate as desired.</p>

<p>Yield: 18-20 muffins</p>
